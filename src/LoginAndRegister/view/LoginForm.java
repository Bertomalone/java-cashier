    boolean cekLogin(String user, String pass) {
        boolean hasil = false;
        try {
            role = daoRole.getByUsername(user);
            if (user.equals(role.getUserName()) && pass.equals(role.getPassword())) {
                hasil = true;
            } else {
                hasil = false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return hasil;
    }
    
