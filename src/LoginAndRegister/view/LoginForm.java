    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        PreparedStatement stmt;
        ResultSet rs;
        String uname = jTextField_UName.getText();
        String upass = String.valueOf(jPasswordField_UPass.getPassword());
        String userType = String.valueOf(jComboBox_UserTyoe.getSelectedItem());
        
        String query = "SELECT * FROM `tbl_users` WHERE `u_uname`=? AND `u_pass`=? AND u_type=?";
        
        try {
            stmt = MyConnection.getConnection().prepareStatement(query);
            
            stmt.setString(1, uname);
            stmt.setString(2, upass);
            stmt.setString(3, userType);  
            
            rs = stmt.executeQuery();
            
            if(rs.next())
            {   
                JOptionPane.showMessageDialog(this, "username and password matched and you are logined ad "+rs.getString("u_type"));
                if(jComboBox_UserTyoe.getSelectedIndex() == 0)
                {
                    ADMIN_JFrame admin = new ADMIN_JFrame();
                    admin.setVisible(true);
                    admin.pack();
                    admin.setLocationRelativeTo(null);
                    admin.setExtendedState(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                } else
                {
                    HOME_JFrame homeFrame = new HOME_JFrame(role.getUserName());
                    homeFrame.setSize(350, 300);
                    homeFrame.setVisible(true);
                    homeFrame.pack();
                    homeFrame.setLocationRelativeTo(null);
                    homeFrame.setExtendedState(JFrame.EXIT_ON_CLOSE);
                    this.dispose();   
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Incorect Username or Password");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

