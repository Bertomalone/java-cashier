    private void jButton_delete_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_delete_ActionPerformed

        if(!jTextField_idBuku.getText().equals(""))
        {
            try {
                Connection con = MyConnection.getConnection();
                PreparedStatement ps = con.prepareStatement("DELETE FROM tbl_daftar_buku WHERE id_buku = ?");
                String id = jTextField_idBuku.getText();
                ps.setString(1, id);
                ps.executeUpdate();
                Show_Book_On_JTable();
                JOptionPane.showMessageDialog(null, "Product Deleted");
            } catch (SQLException ex) {
                Logger.getLogger(ADMIN_JFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Product Not Deleted");
            }

        }else{
            JOptionPane.showMessageDialog(null, "Product Not Deleted : No Id To Delete");
        }

    }//GEN-LAST:event_jButton_delete_ActionPerformed

