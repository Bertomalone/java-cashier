    public ArrayList<Books> getBooksList() {
        ArrayList<Books> booksList  = new ArrayList<Books>();
        Connection stmt = MyConnection.getConnection();
        String query = "SELECT * FROM tbl_daftar_buku";
        
        Statement st;
        ResultSet rs;
        
        try {
            st = stmt.createStatement();
            rs = st.executeQuery(query);
            
            Books book;
            
            while(rs.next()) {
                book = new Books(rs.getString("id_buku"), rs.getString("judul_buku"), rs.getString("penulis_buku"), rs.getString("tanggal_masuk"), rs.getInt("harga_buku"), rs.getInt("tahun_terbit"), rs.getInt("stok_buku"));
                booksList.add(book);
            }
            
        } catch(SQLException ex) {
            Logger.getLogger(ADMIN_JFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return booksList;
    }
    
    public void Show_Book_On_JTable() {
        ArrayList<Books> list = getBooksList();
        DefaultTableModel model = (DefaultTableModel)jTable_tabelBuku.getModel();
        // clear jtable content
        model.setRowCount(0);
        Object[] row = new Object[7];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getJudul();
            row[2] = list.get(i).getPenulis();
            row[3] = list.get(i).getTanggalMasuk();
            row[4] = list.get(i).getHarga();
            row[5] = list.get(i).getTahunTerbit();
            row[6] = list.get(i).getStok();
            
            model.addRow(row);
        }
 
    }
    
